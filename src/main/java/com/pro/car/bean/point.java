package com.pro.car.bean;

import java.awt.*;

public class point {
    Boolean flag;
    String partname;
    Integer x;
    Integer y;

    public Boolean getFalg() {
        return flag;
    }

    public void setFalg(Boolean falg) {
        this.flag = falg;
    }

    public String getPartname() {
        return partname;
    }

    public void setPartname(String partname) {
        this.partname = partname;
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
